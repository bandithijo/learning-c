#include <stdio.h>

/* QUESTION:
 * Write a program to find the area of the rectangular garden provided that the length of the rectangular garden is 15 meters and breadth is 10 meters.
 * */

int main()
{
	int length = 15, breadth = 10;
	int area = length * breadth;

	printf("Area: %d", area);

	return 0;
}
